SELECT ЛекарствоКод, SUM(количесво) AS КоличествоПокупок
FROM ТБЧ_СписокПроданныхЛекарств
GROUP BY ЛекарствоКод
ORDER BY ЛекарствоКод;
GO

SELECT
	Подзапрос.*,
	СПР_Лекарства.*
FROM
	СПР_Лекарства
	INNER JOIN (
		SELECT ЛекарствоКод, SUM(количесво) AS КоличествоПокупок
		FROM ТБЧ_СписокПроданныхЛекарств
		GROUP BY ЛекарствоКод
	) AS Подзапрос
	ON Подзапрос.лекарствоКод = СПР_Лекарства.id
ORDER BY СПР_Лекарства.id;
GO
